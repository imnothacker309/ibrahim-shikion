function randomFrom(array) {
    return array[Math.floor(Math.random() * array.length)];
}


function changeDuaa1() {
    document.getElementById("duaa1").innerText = randomFrom(duaSunnah);
}

function changeDuaa2() {
    document.getElementById("duaa2").innerText = randomFrom([...duaQuran, ...duaGeneral]);
}

function changeZikr() {
    document.getElementById("zikr").innerText = randomFrom(azkar);
}


let count = 0;

function increment() {
    count++;
    document.getElementById("counter").innerText = count;
}

function resetCounter() {
    count = 0;
    document.getElementById("counter").innerText = count;
}


changeDuaa1();
changeDuaa2();
